                <span style="    text-align: right;display: block;font-size: 0.8em;    padding: 10px 0;">---小牛新财富庆小牛资本5年司庆会</span>
                <p>2016年6月6日是小牛资本5年司庆日，小牛新财富在总部瀚森大厦17楼大厅举办了小牛资本5年司庆会。小牛新财富总裁毛锋、总部中后台、入职三周年老员工及深圳区域中后台参加了庆祝会。</p>
                <img src="/data/img/findImg/fing06_01.jpg"/>
                <p>在会上，总裁毛锋对公司的成长历程进行了回顾，并对公司未来的战略、形式及举措进行了解读。毛总说到：“在未来的五年里，我们首先需要更加‘专业化’。我们经历了激情创业的5年，创业难，守业更难，现在我们面临着第二次创业。在守住已经创造的成绩基础上，再去创造新的业绩，这就对我们每个人提出了更高的要求，要让自己更加专业化。其次，我们需要‘科技化’。科技化意味着创新，目前牛鼎丰正在为新财富开发‘智能理财机器人’，可以根据客户的不同需求，将国际上所认同的理财方法通过科技展现在客户面前，我们希望未来的财富管理能更加智能化。再者，我们会更加‘多元化’。新财富目前主要为客户提供的是国内资产配置，未来我们将在海外寻找好的项目，可以为客户提供全球资产配置。在未来，新财富将跟随小牛资本集团，把自己打造成更好的、客户更信任、更喜爱的金融财富管理公司。我相信未来的发展，一定离不开在座每一位的努力，希望大家一起创造小牛更加辉煌的明天。“</p>
                <img src="/data/img/findImg/fing06_02.jpg"/>
                <p>本次庆祝会同时邀请了新财富深圳地区入职三周年的老员工参加，并为他们进行了表彰及颁奖。在颁奖仪式上，毛总对他们的成绩与成长给予了肯定，并对他们的付出表示感谢。</p>
                <img src="/data/img/findImg/fing06_03.jpg"/>
                <img src="/data/img/findImg/fing06_04.jpg"/>
                <p>小牛资本5年之际，集团推出了小牛形象代言公仔。庆祝会上，小牛形象代言公仔首次亮相，并为在场的员工带来了印有公仔形象的幸运小礼品。</p>
                <img src="/data/img/findImg/fing06_05.jpg"/>
                <p>最后，全体人员参与了“祝福小牛资本5岁生日快乐”创意涂鸦送祝福活动，将满满的祝福之情通过大家的创意协作表达出来。本次司庆会圆满结束，愿小牛越来越牛！</p>

