                <p>华为迁出深圳这则新闻为什么引发当地官方如此强烈的焦虑感？就在于华为已将终端生产部门搬迁至东莞，并在去年成为东莞营收与纳税第一大户。《不要让华为跑了》一文称，“1-2月，华为产值占我区（深圳龙岗区）规模以上工业总产值的47%以上，并且产值增速将近40%，比全区水平高出将近25个百分点，若剔除华为，我区规模以上工业总产值则下降14.3%。”</p>
                <p>深圳政府的慢也许是一个“让华为跑”的原因，“以吉华路（坂田段）为例，2009年完成立项， 直到2011年底才开工，目前施工仍在进行，”以及华为周边的医院、学校、商场等生活配套设施不健全。</p>
                <p>文章还表示，深圳市政府攒的“华为科技城”的局也让华为大为恼火。如果你跟华为的人打过交道，你会知道华为是一家相当喜欢且十分擅长低调的企业，与当前浮躁的互联网公司作风不同，这家企业不喜欢张扬、不喜欢抛头露面，更别说被“揩油”了。</p>
                <p> “龙岗区政府宣称，‘华为科技城’由政府投资110亿，华为投资200亿，共同打造科技新城。里面有华为国际会展中心、华为控股全球总部等等好东西。”随后，这个地方不断挤进来各种地产项目，而且都是商业、住宅开发商。各类商业项目全都打上了华为的标签，大肆推介，以至于连华为员工都搞不清楚“华为科技城”是不是跟华为有关。最后逼着华为对内部员工澄清，“华为科技城”与华为没有任何关联，华为既不投资也不购买。</p>
                <p>其实早在2012年，华为总裁任正非正式宣布，将华为终端公司迁移至东莞松山湖，主要发展与手机等所有终端关联的研发、销售和增值业务。据了解，东莞松山湖是10余年前由广东省人民政府批准设立，2010年前经国务院批准的国家高新技术产业开发区。松山湖坐落于“广深港”黄金走廊腹地，地处东莞几何中心，南临香港、深圳，北靠广州，地理位置十分优越。松山湖规划面积72平方公里，坐拥8平方公里的淡水湖和14平方公里的生态绿地，是一个在国内具有示范意义、人与自然和谐共存的科技新城。</p>
                <p>近年来，松山湖通过不断完善基础配套设施，改善居住环境和教育氛围，引进华为、漫步者、华中科技、电子科大、中移动、生益科技、正大集团等等数百家知名企业，从“国家级”的松山湖跃变成“世界级”的松山湖，发展速度惊人，前景可期。这就不难理解为何华为终端总部也迁到松山湖来了。</p>
                <p>事实上，松山湖魅力早在几年前已散发出来，除了陆续引进高新科技企业，还吸引了世界知名酒店进驻，其中以凯悦酒店为最典型。凯悦酒店集团（HyattHotels and Resorts）是世界知名跨国酒店集团，在世界各国管理数百间酒店。集团总部设在美国芝加哥，集团旗下君悦及柏悦全部属五星级酒店，以豪华驰名。</p>
