                <p>
                    而任正非曾在今年3月19日接受新华社采访时含蓄地吐槽深圳的房地产太多，这也给了外界“华为要搬离深圳”的想象力。
                </p>
                <p>
                    任正非表示，高土地价格，高房价，已经导致了生产要素成本急剧上升，必然导致中国企业的竞争力下降，这是发自灵魂的呐喊，是中国最优秀的企业家对中国当前企业生存现状的担忧，是对中国企业经济力下降的担忧。
                </p>
                <P>
                    中国过去十年，是房价高速上涨的十年，也是中国经济竞争力迅速下降的十年，在各个行业，除了垄断国企的规模越来越大，但是企业竞争力却越来越弱，所有的企业都不堪重负。
                </P>
                <P>
                    这也许是你最近看过的最可悲又励志的创业故事：一个深圳商人，10年前以80万元的总价卖掉了一套自己在南山的大房子，还掉银行贷款后，手拿着剩下的60万元去创业。经过这几年努力，白手起家的公司终于开始走上了正轨。辛苦打拼赚到了400多万元纯利润。然后，他用这些钱做首付，把自己当初卖掉的那套房子又了买回来。
                </P>
                <P>
                    假设有个人2005年举家之力，投资50万开了家公司，苦心经营，收益喜人。他能赚得多少回报呢？一般的企业，十年如一日的，纯利润在20%就不错了（股神巴菲特的年收益为20%多），能达到30%就更不得了。姑且按30%计算，按复利计算，每年都把所有的利润拿出来继续投入公司，10年后，50万的公司会变成680万。
                </P>
                <P>
                    但是如果，2005年，同样是这个人，同样是手中的50万元，在最具代表性的深圳宝安中心区全款买套房子，买时均价5000左右，现在单价在6万多，总价值轻轻松松达到了600多万。
                </P>
                <P>再假如，当初他稍微有一点投资意识，还是50万，按照当时2成首付，买上5套房子的话，如今价值多少？创业还是炒房？是一笔让越来越多的人算出来有些惊心的账，尤其在深圳。</P>
                <P>中国过去十年是中国企业家精神丧失的十年，所有人都开始了投机，高房价逼迫所有人都想做房地产，高房价逼迫所有人都想去炒房，很少人、很少企业静下心来提高企业的竞争力。</P>

