                <p>从1965年到1980年，香港的经济命脉都是纺织成衣产业，而机械、造船、电子产业却没有发展起来。与之完全不同的是，韩国、中国台湾及新加坡都像日本一样，由以劳动密集为主的纺织服饰转变为以电机机械设备为主的技术密集工业国家和地区。本来香港在20世纪80年代还有机会，但是香港又有了一个独特的优势：工厂可以北迁，即把自己的制造业基地转移到劳动力成本低廉的广东。数据显示，1979年到1996年内地引进的外商直接投资里面60%来自香港，而广东省这个比例高达80%。1996年港商制造业企业在珠三角直接投资有600多亿港元，40多万家企业，直接雇用了400多万人，而直接为这些企业服务的还有100多万人。之所以引用这些数据，只是为了说明一个问题，港商曾经是多么辉煌，但这种辉煌我们今天却看不到了。根据香港工业总会2013的数据，港商在珠三角的企业只有7万家。实际上，珠三角现在比较知名的企业都没香港人什么事儿，比如腾讯、华为、比亚迪。</p>
                <p>是什么打垮了香港企业家？很简单，泡沫经济！数据显示，从1991年到1997年这7年间，香港房价和股价的增值一共是7万亿港元，相当于香港在这7年间GDP的总和。也就是说，1997年以前香港所有的财富都被股市和楼市的泡沫吸走了。除了少数在卖空中大赚特赚的投机者，大部分人这么多年的辛苦所得全部化为泡影，整个社会的财富也都付之一炬，社会财富被掏空的结果就是经济陷入长期衰退。</p>
                <p>泡沫经济毁掉的不仅仅是财富，更是创业热情、公众信心和社会基础。因为泡沫经济崩溃之后，中产阶级都沦为事实上的房奴，这样整个经济都由创业经济变为收租经济，经济不管是涨还是不涨，都是拥有土地的少数人借由租金获得越来越多的财富。1997年香港人均收入28000美元、2011年32000美元，90%的港人生活条件在过去十几年里没有得到改善，财富越来越集中在少数人手中，1%的人占去社会总资产的43%，5%的人占去72%。</p>
                <p>香港成为全球房价负担最重的城市，（北上深的房价收入比其实早已超越香港）。大部分人为六七十平方米的房子一生都要背上沉重的债务。反观我们，为什么华为会如此牵动着深圳人的敏感神经？这值得我们更多的思考。</p>
