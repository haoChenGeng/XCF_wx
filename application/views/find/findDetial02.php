                    无论是铺张浪费，还是流程老化，部门区隔或工作推诿，都只是表面现象，究其本质是运营管理和企业斗志出现了偏差。小牛现阶段出现的问题，应通过精细化管理，降低浪费，提高效率，人尽其责，物尽其用，形成“背靠背”的协作和信任文化，让组织高效率地运转，管理升级推动发展升级，以文化感召推动员工信任协作。让山头主义、怀疑主义和推诿主义，都统统不见。胜则举杯相庆，败则拼死相救。小牛年初就提出了“创新年”的口号，要求各业务线将创新发展摆在首位，实施人才升级计划、推动各业务板块模式升级、推动大数据征信等技术升级、制定更为完善的人才引进、薪酬绩效、行政办公、采购运作等制度设计，促进管理升级、遏制铺张浪费、精简冗余人力、抵制“未富先奢”的享乐、杜绝行业陋习、用积极的心态看待经济环境和行业氛围……是的。我再次强调，小牛的永久青春力是由凌厉思考、艰苦奋斗、厉行节约和信任协作所构成的。只有青春力被唤起，小牛才可以走得更远、更强健。
