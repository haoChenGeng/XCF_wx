                <p>
                    正如人不能生而强大，企业也有一个从萌芽、发展到壮大的过程，呈现出一定的阶段性。 在创建早期，企业往往不缺乏经济学家凯恩斯所说的“高涨的动物精神”，好比华为的“狼文化”：激情、忍耐、进取、追求、牺牲精神、一味的乐观和强大的进取力。激情与奋斗不仅是绝大多数企业诞生的精神支柱，更是企业持续发展、永葆活力的精神源泉。站在发展的第五个年头，小牛资本管理集团从“搭框架、抢市场”的初创阶段、到“占风口，重规模”的粗放式发展阶段，形成了涵盖小牛普惠、小牛在线、小牛分期、小牛新财富、小牛投资五大业务版块，形成以普惠金融、消费金融、财富管理及投资管理为主营的综合性金融服务集团，目前迈步进入创业的第三个阶段，即“有地位、重效益”的阶段。这意味着，小牛的发展方式也从扩张粗放转为深耕细作。然而，在这一企业发展的关键阶段，有部分人却认为小牛“做大”了，小牛正在成为公众公司，小牛已然小有成就……却忽视了小牛资本其实远未企及起设定的目标高度、仍处于创业阶段的事实。创业即意味着我们在资源、市场占有、人才、技术、品牌和管理等方面依然不足，仍有很大的提升空间。在强敌环伺之下、在经济大环境趋寒之下、在新技术新模式层出不穷的变革时代，只有继续怀着艰苦奋斗、奋发向上、勤俭节约的进取精神，时刻保持危机和忧患意识，付出超越常人的辛勤和汗水，我们才能抓住难得的历史机遇，在风浪搏击中赶超对手。艰苦奋斗，勤俭节约。领导干部不仅是传播者，更是践行者。要改变片面追求规模的经营理念，从“重规模、轻效益”转到“效益第一，兼顾规模”上来。要提高员工的整体素养，提高人均产能，公司整体效益要从员工数量驱动转向个人产能驱动上来。要有经营成本意识，将利润作为最核心的考核指标。以管理促发展，向管理要效益。艰苦奋斗，勤俭节约，要从每个部门、每个岗位着手。比如，采购部门要严格控制，大到IT设备，小到一张纸、一支笔，都要有精打细算、节约成本的意识；人力部门要提高招聘的有效性，精简冗余人力，既要对企业负责，也要对员工负责，不能走入招人--辞人--招人的恶性循环，造成资源的极大浪费；财务部门要有效地进行税务筹划；资金部门要检查自己的工作细项，整合内部资源，提升议价能力，减少公司的运营成本；行政部门要合理规划职场使用，改善行政效能，减少资源消耗；业务部门要以结果为导向，制定科学目标、不打折扣不畏艰难，要承担不达标的后果。
                </p>
