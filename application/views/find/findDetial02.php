                <p>
                    立足于“创新金融”的本义，小牛资本面临的挑战，恰恰源于发展和创新的挑战。一方面，我们要继续发扬艰苦奋斗的精神，在合规的前提下勇闯新路，在业务模式、产品、营销、服务上不断优化升级甚至另辟蹊径，抓住金融为实体经济、小微经济和个体服务的主线，践行普惠金融，坚持“美好资本、美好金融、美好社会”的理念；另一方面，小牛正处在从粗放转精细发展的关键时期，我们要不断提升管理和运营水平，把公司做小做精，把客户做大做足。知易行难，任重道远。作为员工人数较多，分支机构超300家的综合性金融服务集团，小牛资本从规模上已脱离了“小公司”的范畴，但思想上却并未见大，未达到成熟企业的江湖地位和市场份额，未达到大企业的格局和胸襟，染上了一些“大企业病”：一些物资、人力、时间和系统上的铺张浪费，随处可见；一些流程上的老化、部门上的山头、协作上的矛盾、责任上的推诿，随处可见；一些大势判断上的模棱两可、经验主义主导认知，随处可见。或许有人认为公司家底越来越厚、物质条件大大改善，讲排场、比阔气不是什么大事，却不知我们每节约一分钱，就会使公司利润增加一分，就会让公司的效率提高一分。节约不仅是赢利的源泉，更是提升核心竞争力的途径。“成由勤俭败由奢”，大到治国、小到治企，艰苦奋斗、勤俭节约，绝对不能丢弃。 超出需要的浪费，就像不知不觉中积存下多余的脂肪，妨碍企业的运转，降低企业的效率；流程趋于老化，职能上的区隔造成相互推诿和决策缓慢，认知上的不成长，都将阻碍创业小牛的青春生长力。
                </p>
