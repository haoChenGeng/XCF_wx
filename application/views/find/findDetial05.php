                <p></p>
                <p>2016年6月6日，小牛的司庆，第5个年头。</p>
                <p></p>
                <p>5岁了，我百感交集。时间不算短，岁月不算长。有长者说，“做企业，3年是一坎。”统计上已经证明过，大部分创业企业都活不过3年。小牛无疑过了统计学上的一关，而且还在飞速长大。小牛资本管理集团目前有5大业务板块：小牛普惠、小牛在线、小牛分期、小牛新财富、小牛投资，涵盖“普惠金融、消费金融、财富管理及投资管理。5岁的小牛，员工两万，分支三百，强健壮美，铿锵有力。</p>
