                <p>创业时的情景，依然历历在目。当时做过大势研判，随着中国金融抑制的逐渐解除、金融深化和自由化大幕的不断拉开，一定需要多元化、专业化、普惠化的多层次民间金融供给。中国一定是个金融供给的大国，虽然国有金融机构占据主导，但是中国金融生态是个大尺度生态，供给侧改革也会发生在金融领域。小牛愿意并应当参与到这一激动人心的潮流之中。</p>
                <p>创业的旅途，有风景也有荆棘，误解总会跑在理解的前面，各种甘苦、各种纠结、各种变数、各种挫败、不能一一而论。关键在于，小牛依然强健向前，没有止步。现在的小牛，不能说大成，也算是小就。在第5个年头，就有老员工问我：我们见证小牛超越预期的发展，相信小牛肯定做对了一些事情？小牛存在哪些问题？小牛的下一个5年会用什么样的方式奔跑？</p>
                <p>创业肯定要带着完整的想法去创业，但不可能把所有路线都想好，顶尖棋手下棋最多也只能计算未来3步怎么走。创业也不是一个随心所欲拿到资源搭建企业的过程，创业会有路径上的约束，会有各种“差一点”、会有各种短板、会有各种限制。我最近看小米的报道，从风口企业似乎变成忽悠企业，褒贬转换之快之轻率，令人唏嘘不已。此前的“口碑和参与感”变成了“小米营销是邪路，华为研发是正道”。有时候我就想，就3年前小米的技术能力和资源禀赋来说，能做到一年出货7、8千万台，需要不需要在初始阶段采取“互联网营销”呢？当小米在快速奔跑时候，如何把握时间点，回归基本功，将重心转移到技术研发上来？</p>
                <p>所以，选择路径，都有更为复杂的理性、约束和痛苦。但是，小牛把握一条，理性可以有所不及，但我们的初心不能变，我们的价值观不能变。这种不能变有时候在变化时代就能做出最好的选择，即使是用最长远的波段看，这也是最好的选择。初心，宛如幸运星。</p>
                <p>小牛的初心就是“美好资本、美好金融、美好社会”：金融是为实体经济服务的，金融应该覆盖更大的人群，金融应该更平等更美好，金融需要更好的技术更新的风控手段来推动。金融不仅仅有商业属性，也应该有社会属性。好的金融，会让诚实的穷人获得境遇上的改善，会迫使骗子遵守规矩，会为踏实的企业提供机会，会让每一个人爱惜羽毛、尊重声誉、积累社会资本。小牛小牛，就是让平凡的小人物感到自己很“牛”，这该是多么美好的一个社会。</p>
                <p>小牛以投资管理、财富管理起家，考虑到中国处于财富升级、投资升级、消费升级的宏大背景，这一方向必然没错。但小牛也发现另外一个维度，就是金融的“卷入效应”。过去没有交易的产权被“卷入”，例如农村的土地流转。很多已经没有意识或者没有条件纳入金融交易的资产开始产权化（确权）、定价化、金融化，这意味着金融的广义覆盖、普惠化、平等化正在急速发生。</p>
                <p>前一个维度，小牛主要是管好别人的钱、投好的资产项目、赚取的是管理费用，考验的是发现好的资产端的能力。高净值人群的大额资金，匹配优质的大额资产（一般是机构资产）。</p>
                <p>后一个维度是，小牛同样要管好别人的钱，但要创造另外一些人进入金融的机会，考验的是小牛创造资产端的能力。老实说，如果小牛不是抱着“美好资本、美好金融、美好社会”的初心，如果是图舒服，不做脏活累活笨活，根本就没有必要理会后一个维度，没有必要布局小牛在线、小牛普惠和小牛分期。现在，小牛在线成为线上的小额资金端，且只负责资金端；而小牛普惠和小牛分期成为线下的小额资产端，且只负责资产端。小牛普惠和小牛分期建立超过万人的线下资产端队伍来寻找靠谱的小额借款人，解决他们的急需、审核他们的风险，为小牛在线的小额资金端（用户）提供靠谱的资产。小额分散的资产匹配小额分散的资金。</p>
                <p>“资产端战略”是小牛的核心战略，发现和创造资产端的能力，让资产端不断升级的能力，就是让资金有靠谱的资产端对应，就是让金融不断“实体化”的过程，就是“实体金融”。“实体金融”就是支持实体经济的金融形态。相反，如果宏观上发生“货币空转”——就像几年前流行“票据反复贴现”，或者微观上发生没有资产对应的纯资金端游戏——庞氏骗局，都是金融泡沫化、危机化的证据，都是金融监管真正要根治的对象。</p>
                <p>有人说，小牛善于踩中政策的鼓点，在政府鼓励普惠金融的时候，早就有了普惠，在政府鼓励消费金融，早就有了分期。我认为这是对小牛智力上的过誉，其实小牛的选择并不是因为眼光，而是坚持小牛的“实体金融”的信念，认识到金融在两个维度的变革，没有被经验主义所拖累，没有被以往的路径所锁定。小牛的大胆布局，不仅仅是战略上的布局，也是信念上的必然。</p>
