                <p></p>
                <p>下一个5年，依然是在两个维度上展开思考。</p>
                <p></p>
                <p>小牛坚持“资产端升级战略”。今年是“金融监管年”，我们的理解，政府警惕的还是资金端，警惕的是那些用建立庞大线下人力去寻找资金的公司，用庞大的人力去拉客户的钱的公司，而不是那些用庞大线下人力去寻找借款人的公司，不是那些庞大线下人力去寻找靠谱资产的公司。政府警惕的是资金池，是假标，是自融，而不是警惕那些找资产端的公司。</p>
                <p>未来5年，在第一个维度上，小牛不仅在国内继续发现优质资产，也要在全球发现优质资产。财富管理投资管理要全球化，要为客户全球资产配置。试水海外，任重道远，小牛已经在香港设立管理中心。趋势所在，箭在弦上，不得不发。</p>
                <p>未来5年，在第二个维度上，小牛会坚持“资产端重、资金端轻”、“资产端线下化、资金端线上化”、“小额短期资金匹配小额短期资产”这一“小牛模式”，在这过程中，不断纯化、强化和深化模式，升级数据征信，优化风控流程，提高经营效率，覆盖更多的人群，让普通大众也能享受较高的理财收益，让诚实借款者得到改变境遇的金融机会。让风险得到准确衡量，让金融变得更美好更平等。</p>
                <p>未来5年，小牛会继续相信金融科技的力量，相信人才升级的力量。没有金融技术，没有fintech，很多创新就是无源之水无本之木。但fintech也不是万能的神，“五色令人目盲，五音令人耳聋”，正确技术的正确筛选、正确筛选后的正确引入，正确引入形成的商业模式的优化，背后恰是人才的力量。没有优秀的人才，就不可能有进步的组织和蓬勃的企业家精神。技术驱动模式，人才选择技术。针对人才升级，小牛提出了“利益共同体、责任共同体、命运共同体”的共享计划，研究长期股权激励计划方案。“外部人进来，内部人进步”，大家要认同“实体金融”，才可能分享“美好小牛”。</p>
                <p>未来5年，小牛会成为一个温暖的品牌，要让大家都觉得小牛是“有温度的资本”。小牛是专业的、温暖的、美好的。在我的理解里，企业的品牌，就像人的品行。如果一个人，他/她是有文化的专业人士，同时，做人温和得体，愿意为别人着想，热爱家庭，热爱运动。那么，这样一个人就符合小牛人的调性。</p>
                <p>所以，小牛热衷学术活动，传递理性观点，赞助家庭秀场，赞助大众运动。这其中的内在逻辑是一体的。小牛为什么天生有运动基因？小牛发现，热爱家庭热爱运动的人对未来有着更积极的看法，所以他们不抑郁、乐观、喜欢孩子、关注长远利益、注重个人的金融信用。所以，小牛的品牌逻辑和小牛的金融逻辑，是一致的，是彼此深化、相互促进的。</p>
                <p>印度央行行长拉詹在其著作《从资本家手里拯救资本主义》写道，“一个健康的、有坚定原则和竞争力的金融市场，对于经济发展、科技创新和抗击贫穷有着深远的作用。”是的，小牛认为金融不仅帮助经济更好地配置资源，而且，金融也应该更好地促进社会道德资源。</p>
                <p>当然，有时候，金融的吊诡和复杂，我们暂时是看不懂的。去年被说成对的事情，今年可能就是错的。去年是“风口”，今年要“封口”。先贤黄遵宪说，“我手写我口、我口写我心”，我们坚持初心，坚持美好金融的原则。在这个前提下，我们设计和更改我们模式、我们设计和更改我们的角色，我们设计和更改我们的路径。我们获得了我们应该成为的那个自己。</p>
                <p></p>
                <p>最后，祝全体小牛人节日快乐！</p>
