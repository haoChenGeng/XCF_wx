            <p style="box-sizing: border-box; line-height: 1.75em;"><span style="font-size: 14px;"><span style="font-size: 18.0180187225342px; line-height: 31.531530380249px;  background-color: rgb(255, 255, 255);">　　</span>在不确定的背景下，投资应该穿透迷雾，把目光回归本源，关注那些较为确定的因素。对于2017年，有专家认为，看好中国市场的投资机会。在经济增速下行态势趋稳之后， 经济结构转型的一些效果开始体现出来。较低的利率水平配合财政政策的投入，股市和债市都会有所表现。</span></p>
            <p style="box-sizing: border-box; line-height: 1.75em;"><span style="font-size: 14px;">&nbsp;</span></p>
            <p style="box-sizing: border-box; line-height: 1.75em;"><span style="font-size: 14px;">　　国内A股方面，总体来看，2017年价值股和成长股将站在同一起跑线上。2016年，以保险资金为代表的大资金已配置了较多的大盘股，抬升了大盘股的整体估值；另一方面，成长板块则经历了较长时间的调整、震荡，不少优质成长股的盈利能力经受住了市场的考验。2016年二、三季度的数据就显示，很多白马成长股的净利润水平依然保持了20%~30%以上的同比增长，同时股价又有一定的回调，导致成长板块出现了一批业绩增长确定、估值相对便宜的价值成长股。因此，2017年投资者应将重点关注两方面的投资机会：一方面继续看好低估值、分红收益率高的股票，另一方面看好未来增长确定、近期估值又有所回落的白马成长股。此外，如果中美两大经济体均在2017年发力基础设施建设，也有可能带动一批原材料周期股的行情，例如焦煤、铅锌等品种，投资者可紧密关注。</span></p>
