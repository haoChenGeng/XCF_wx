            <p>
                比如，如果美元在全球市场走弱时，人民币的锚主要是以美元为主。这可以避免在美元汇率指数大幅走弱时，人民币对美元出现被动地大幅升值。当美元在全球市场出现较大反弹时，人民币对一篮子货币价格将成为人民币的锚，即中国可以保持人民币兑换一篮子货币的稳定，甚至可以轻微提高人民币指数。这样同样可控制人民币兑换美元的波动度，而不改变人民币对美元的贬值趋势，但保持人民币指数的稳定，同时避免可能出现的人民币的恐慌性抛售潮。	
            </p>
            <p>
                当然对这种解释看上去是有一定的合理性，但并不全然，因为这仅是从自己的角度而不是从市场角度来观察人民币汇率的变化。因为，在目前以美元为主导的国际货币体系下，国际市场更多关注的美元对人民币汇率变化，而不是观察不同汇率指数下人民币汇率的变化。美元对人民币汇率的变化是决定投资者确定人民币是升值还是贬值最为基本的根据，其投资策略往往以此而动。对此，这是中国央行不得不考虑的重要因素。
            </p>
            <p>
                而这次中国央行引导人民币贬值没有引发人民币的恐慌性抛售，最为重要的还在于中国央行有事先准备，有新的制度安排应对人民币炒作者的冲击。所以，在当前的经济形势下，或美联储货币政策将全面收紧的情况下，中国央行要先有准备，以此来保持人民币汇率的稳定。而要做到这点，中国央行要让人民币汇率制度更为公开透明，并给市场一个清楚明确的预期。当然，投资者也不得不注意中国央行正在引导人民币逐渐贬值。
            </p>
