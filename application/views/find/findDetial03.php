                <p>从5月中旬以来，全球货币又开始波动，因为受市场避险情绪及美联储加息预期重新升温的影响，美元大幅反弹，美元汇率指数也由5月3日91.92低点升至95.60附近，快速上涨4%，从而涉及全球货币波动，特别是亚洲货币出现波动，使得人民币在内亚洲新兴市场货币兑换美元出现不同的程度贬值。同时，这次人民币兑换美元中间价的下跌幅度累积达1.7%，贬值幅度已经超过了年初1月的贬值幅度1.3%。
                </p>
                <p>也就是说，尽管这次人民币的贬值幅度也不小，即贬值速度要快速于1月份初，贬值幅度要大于1月初，但1月初的人民币兑换美元贬值则引发一轮人民币恐慌性的抛售潮，从而造成了国内外市场剧烈波动。在离岸市场，人民币即期贬值也没有导致人民币期权波动率大升，目前3个月的波动率远低于今年初水平，显示市场对人民币的预期还是较为稳定的。</p>
            </div>
            <div class="text01 text02">
