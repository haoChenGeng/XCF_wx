                <p>
                    那么在这一轮人民币贬值中市场反应为何会如此平淡？这可能与中国央行先有预案准备有关。特别是经过2015年8月及今年1月初人民币贬值所造成对市场的巨大震荡，中国央行也对汇率制度进行检讨，并全面在在岸市场及离岸市场做了一些新的制度安排，而且用这些新的汇率制度安排全面击退了人民币汇率的炒家。因此，当新一轮的人民币汇率贬值出现时，国际市场的投资者不敢轻易地进入市场，还得观察市场之变化。
                </p>
                <p>
                    而且，这次中国央行在引导人民币兑换美元贬值的同时，国内市场还突然出现“美元荒”，隔夜美元兑换人民币一周期和一月期掉期都创两年新低。对于国内市场突然出现的“美元荒”，有分析认为，这可能是国内市场美元流动性紧张所致，也可能是与美联储加息预期升温有关，更不排除大行在掉期市场“奉命结汇”的可能，以此来缓和未来人民币可能面临的压力。
                </p>
                <p>
                    同时，也有分析认为，这次人民币贬值市场如此淡定，可能是与近期中国央行所采取的人民币双锚策略有关。所谓的双锚策略主是根据美元在全球的变化，通过切换使用美元或篮子货币这两个定位锚，来控制美元/人民币的波动率。
                </p>
