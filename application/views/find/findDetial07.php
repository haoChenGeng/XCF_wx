				<p>在美国CFA协会4月份的金融科技调查中，智能理财被认为对金融行业影响最大，这又是另外一个比较陌生名词。智能理财是人工智能在金融领域的最早实践，是一种低成本的线上理财服务，主要是依据用户不同的风险偏好、投资目标及资产规模等数据，来提供自动化的，以算法为基础的投资组合管理建议。咨询公司科尔尼发布的一份报告中曾预测，美国智能理财服务到2020年，将会管理5.6%的美国投资资产，而2015年仅为0.5%。</p>
				
				<p> 智能理财是不是真的对用户产生了价值？对于这个问题，笔者目前的答案：是，也不是。</p>
				<p>之所以说"是"，是因为智能理财在解决一个有价值的问题，特别对于中国普通大众的理财需求。在中国当前的理财市场上，大多数普通老百姓一直在面对一个难题，就是怎么三选一。在中国市场上，真正可以供老百姓选择的理财品类其实并不多，大致就三类： A类，存款及类存款的理财产品(银行理财等)，特点是基本有国家信用支撑，不太担心违约，但收益率很低(3%-5%之间)，跑不赢通胀；B类，权益类产品，股票、基金等，价值波动非常巨大，年均-50%-50%之间，搞的好了能挣不少钱，搞得不好就被人当韭菜收割了；C类，中高风险债券，包括P2P、信托等等，特点是收益率不错(9%-12%之间)，但存在不低的违约风险，违约后可能血本无归。</p>
				<p>如果从一个纯理性的角度分析，上述3类产品中，单纯的选择A、B或者C，无疑都不是一个最优的理财选择，最优的选择一定是混和ABC的一个组合或者一个动态的策略。那如何得到最优呢，毫无疑问这个问题是有解法的，而这个解法也一定通过某种数量化的计算可以得出，这就是智能理财的用处和价值。</p>
				<p>而从另外一个角度来看，上述观点在现实中也有局限性。因为上述的假设是从事智能理财业务的平台，能够在市场中找到最优的A类、B类和C类资产。</p>
				<p>这里面的道理是简单明了的，如果一个算法或者一个策略，无法拿到最优的A、B和C，那不论他怎么组合或者配置，肯定是无法得到最优解的，原料不好，再怎么加工都很难烹饪出最美味的佳肴，这个道理人人都懂。市场的现实的确也是如此，比如市场上有些平台，他的组合策略几乎完全依赖C类资产(P2P)去做，毫无疑问这个没法达到最优，而有些平台，又完全依赖证券化资产(基金、股票)去做，也是一个道理。</p>
				
