				<p>P2P和众筹近几年大家已在互联网上听过不少，区块链和智能理财却是两个相对陌生的名词，而区块链这个名词近期却在媒体上开始频繁露脸。</p>
				
				<p> 4月19日，中国首个区块链联盟中国分布式总账基础协议联盟（ChinaLedger联盟）在北京成立，该联盟由中证机构间报价系统股份有限公司等11家机构共同发起。</p>
				<p>5月31日下午，聚焦于区块链在金融方面应用的金融区块链合作联盟（深圳）正式成立。该联盟集结了包括微众银行、平安银行、招银网络、恒生电子、京东金融、腾讯、华为、银链科技、深圳市金融信息服务协会等在内的31家企业，其中发起单位25家，腾讯、华为等6家机构作为成员单位加入。</p>
				<p> 什么是区块链？它到底有什么用？根据百度百科的解释，区块链（Blockchain）是比特币的底层技术，像一个数据库账本，记载所有的交易记录。这项技术也因其安全、便捷的特性逐渐得到了银行与金融业的关注。区块链技术不仅可用于数字货币，还有其他更广泛的用途，而货币范围的应用被称为区块链。</p>
