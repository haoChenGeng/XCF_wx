                <p>近日，全国科技创新大会、两院院士大会、中国科协第九次全国代表大会在京召开。会上，中共中央总书记、国家主席、中央军委主席习近平发表重要讲话指出：科技是国之利器，国家赖之以强，企业赖之以赢，人民生活赖之以好。中国要强，中国人民生活要好，必须有强大科技。</p>
                
                <p>当今之世，科技创新能力成为国家实力最关键的体现。在经济全球化时代，一个国家具有较强的科技创新能力，就能在世界产业分工链条中处于高端位置，就能创造激活国家经济的新产业，就能拥有重要的自主知识产权而引领社会的发展。正是因为科学技术具有如此的重要性，我们的国家领导人也在多种场合提出大力发展科学技术。</p>

                <img src="/data/img/findImg/fing07_1.jpg"/>
				<p>而科技对金融的影响同样不可小觑，证券市场从最原始的叫喊竞价到电话下单，再到电脑下单、手机下单，科技的发展让投资们对金融领域的接触更加便利，也让投资者更加能快捷的投资金融标的。作为经济发展的血液和重要支撑，金融对科技的发展从来是敏感而又相辅相成的。2016年4月，美国CFA协会发布过一个金融科技调查报告，共有3803位受访者参与调查，其中92%的受访者为CFA持证人，他们认为未来对金融行业影响最大的分别是智能理财、市场借贷（P2P）、众筹以及区块链技术。其中37%的人认为智能理财会在未来1年内对金融行业影响最大，其次是P2P（23%）、众筹（15%）、区块链技术（11%）；有40%的人认为智能理财会在未来5年对金融行业影响最大，其次是区块链技术为30%。P2P和众筹被认为在短期内对金融行业影响较大，在未来的影响会逐渐减小。</p>
