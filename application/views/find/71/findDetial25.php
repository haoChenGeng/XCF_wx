				<p>小牛资本家庭金融战略暨小牛金服启动发布会上，小牛金服正式启动并首次对外公布了“美好伙伴”计划，小牛金服执行总裁刘金科正式宣布推出两项资产端孵化的新业务，并宣布启用NEO FINANCIAL的品牌新logo和“让美好发生”的slogan。</p>
				<strong style="max-width: 100%; box-sizing: border-box !important; word-wrap: break-word !important;"><span style="max-width: 100%; color: rgb(255, 255, 255); box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(136, 136, 136);">小牛资本启动“家庭金融战略”，助力家庭消费新风潮</span></strong>
				<p>在后房地产时代，以房地产为主的家庭金融资产正在被“重构”，启动“家庭金融战略”，小牛的初衷是为大众发现和创造更多优质的资产、创造更多美好的家庭消费项目，帮助每一个家庭有效对抗经济下行收益下降，是小牛一直以来坚守的商业初衷。</p>
				<p>发布会上，小牛资本董事长彭铁发表了创业感言，他认为办企业不仅要做出市值，还要做出价值，利润能保持企业持续运转，而价值则保证企业不因短期的利润而迷航。金融企业应该体现出一种更为宏阔的责任，小牛有义务为社会每一个美好家庭发声，促进全社会的家庭关系更加稳健，促进更多的人热爱家庭。</p>
				<p style="max-width: 100%; min-height: 1em; color: rgb(62, 62, 62); font-size: 16px; line-height: 1.75em; box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(255, 255, 255);"><span style="max-width: 100%; text-decoration: underline; font-size: 12px; color: rgb(255, 104, 39); box-sizing: border-box !important; word-wrap: break-word !important;">☞彭铁表示：小牛向美好发声，也欢迎各界朋友对小牛发声，小牛将汇聚这些美好的声音，用自己的实践发出更壮丽的声音。</span></p>
				<img src="<?php echo $filePath;?>fing25_2.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p> 小牛资本集团副总裁唐学鹏现场进一步解释了属于小牛的“美好家庭金融战略”的构建逻辑：作为一家综合金融服务集团，既有高净值客户的理财，也有普罗大众的投资，我们大多的客户都已经成家，他们将财富托付给小牛，或者信任小牛的平台，小牛就有受托责任，受人托付为人尽责。</p>
				<p>唐学鹏表示，小牛的“家庭金融战略”的本意是帮助社会经济资源进行合理的配置，希望未来能参与更多的家庭资产配置，促进更多的家庭财富实现增长。现场小牛还邀请了金马奖最佳纪录片周浩和中国家庭金融调查与研究中心主任甘犁教授，共同为启动美好“家庭金融战略”发声。</p>
				<p style="max-width: 100%; min-height: 1em; color: rgb(62, 62, 62); font-size: 16px; line-height: 1.75em; box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(255, 255, 255);"><span style="max-width: 100%; font-size: 12px; color: rgb(255, 104, 39); text-decoration: underline; box-sizing: border-box !important; word-wrap: break-word !important;">☞蝉联两届金马奖最佳纪录片导演周浩现场和大家分享了最新的纪录片作品《异乡的父母》，受小牛资本美好家庭计划委托，周浩对中国底层家庭生存状况进行了真实的记录，加入小牛“美好家庭计划”，让孩子回到父母身边，小牛需要你的行动。</span><br style="max-width: 100%; box-sizing: border-box !important; word-wrap: break-word !important;"></p>
				<img src="<?php echo $filePath;?>fing25_3.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p style="max-width: 100%; min-height: 1em; color: rgb(62, 62, 62); font-size: 16px; line-height: 1.75em; box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(255, 255, 255);"><span style="max-width: 100%; font-size: 12px; color: rgb(255, 104, 39); text-decoration: underline; box-sizing: border-box !important; word-wrap: break-word !important;">☞小牛资本联手中国家庭金融调查研究中心，开展了“中国家庭金融资产配置风险研究”，家庭金融研究专家甘犁教授在发布会上发布最新《中国家庭金融资产配置风险报告》。报告显示，房产为主的中国的家庭金融太极端，究其原因是因为中国市场缺少中等风险、中等收益的产品。</span></p>
				<img src="<?php echo $filePath;?>fing25_4.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<strong style="max-width: 100%; line-height: 24.0000019073486px; box-sizing: border-box !important; word-wrap: break-word !important;"><span style="max-width: 100%; line-height: 24.0000019073486px; font-family: 黑体; box-sizing: border-box !important; word-wrap: break-word !important;">孵化资产端两大新业务，建立商业大闭环</span></strong>
				<p>小牛金服旗下囊括了线上普惠金融平台小牛在线、小额分散资产端小牛普惠、垂直细分资产端前海融辉、纯线上自动化借贷平台牛贷项目等四大平台，目标是成为小微企业及年轻家庭超级金融入口，各个平台将在今年底和明年初分别引入多层级的战略投资伙伴，谋求海外上市。</p>
				<p style="max-width: 100%; min-height: 1em; color: rgb(62, 62, 62); font-size: 16px; line-height: 1.75em; box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(255, 255, 255);"><span style="max-width: 100%; font-size: 12px; color: rgb(255, 104, 39); text-decoration: underline; box-sizing: border-box !important; word-wrap: break-word !important;">☞发布会现场公布了资产端孵化出的两项最新小额分散业务，执行总裁刘金科表示，这是表明小牛金服从两点的小闭环，正在变成多点的大闭环。</span></p>
				<img src="<?php echo $filePath;?>fing25_5.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p>
					成立三年来，小牛金服旗下的小牛在线已经成为华南最大的线上普惠金融理财平台，为满足需求端的多元化诉求，受限的供给端提供了有效的解决途径，为过去不被传统金融所覆盖的人群提供了更丰富的家庭金融服务。目前理财用户近400万，累计成交额超过400亿，是最早加入央行指导的中国互联网金融协会成员单位之一。
				</p>
				<p>
					小牛普惠专注于小额无抵押的信用贷，线下网点260多家，累计放款额超过120亿，服务借款人近80万，涵盖网商贷、工薪贷、三农贷、精英贷等优质产品项目，资产优质、分散和多元化，在国内综合行业排名前五。
				</p>
				<p>
					 目前，小牛在线和小牛普惠形成了线上资金端和线下资产端、一轻一重的商业闭环，这种小额分散的商业模式不但与监管方向不谋而合，更重要的是，可以实现商业利润。
				</p>
				<p>
					  “普惠金融需要讲究市场性原则，普惠金融并不等于纯粹的政策性金融或慈善金融，而是要通过‘利率覆盖风险’等方式实现商业上的可触达性和持续性。”两头重的线下小额信贷服务模式，成本高效率低；两头轻的纯线上借贷模式，信用风险剧增，也很难做出规模效益。“目前，无论是小牛在线还是小牛普惠，我们的财务状况都是非常良好的。”刘金科说。
				</p>
				<p> 2016年4月，小牛金服开始了对小牛在线和小牛普惠的整合。“与蚂蚁金服开始于电商模式、腾讯开始于社交模式不一样，小牛金服是从P2P开始的，但小牛金服绝不止于P2P。”刘金科表示，2016年，在小牛金服主导下，两点（小牛在线及小牛普惠）组成的小闭环正在变成多点的大闭环，在资产端新孵化出了同样小额分散的两大新业务：垂直细分资产为主的前海融辉，和纯线上自动化借贷平台牛贷项目。</p>
				<img src="<?php echo $filePath;?>fing25_5.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p>
					  其中，前海融辉是2016年7月新成立的一家以房贷车贷抵押为主的垂直细分类资产平台。孵化至今，线下网点已经覆盖全国31个城市，37家线下门店，预计未来三年内开设超过90家服务机构，覆盖全国一二线城市及个别三线城市，预计2019年累计放贷规模达到100亿。
				</p>
				<p>
					  而即将在今年12月上线的牛贷项目，是基于现时金融服务资源使用效率低下，大量的借贷寻求无法满足的背景下成立的纯线上自动化借贷平台。传统资信评估最顶尖的FICO评分体系，完成抵押贷款的审批则需要0.5至2个工作日，汽车贷款在1小时左右，小额消费信贷需要15分钟。牛贷项目则是依靠自主研发的全流程线上自动化风控体系和贷后预警体系，提取300多个变量，利用6个预测分析模型完成授信，整个审批、放款过程仅需2分钟，为个人客户、小微企业主提供迅速专业融资的在线服务。
				</p>
				<strong style="max-width: 100%; box-sizing: border-box !important; word-wrap: break-word !important;"><span style="max-width: 100%; color: rgb(255, 255, 255); box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(136, 136, 136);"><strong style="max-width: 100%; line-height: 28.4444465637207px; box-sizing: border-box !important; word-wrap: break-word !important;">全面开放“美好伙伴”计划，</strong>小牛金服海外上市</span></strong>
				<p>
					  本次发布会，小牛金服宣布携手旗下四大平台宣布打开闭环，启动“多层开放，共建美好”的美好伙伴计划，在战略伙伴、公司治理及业务拓展上进行全开放式的合作和创新。
				</p>
				<p>
					  刘金科认为，行业已经进入高阶竞争阶段。闭环在初始竞争阶段和环境里，通过自我循环，企业可以获得较好的生存空间。然而在进入高阶竞争阶段的环境里，闭环可能会导致企业战略空间越来越小。“只有打开闭环，才会使我们的战略空间变得无限大。”
				</p>
				<p>
					   美好伙伴计划包括两部分内容，第一部分是打开商业模式闭环，拓展多元的资产及资金合作伙伴。
				</p>
				<p>
					  刘金科认为，当初小牛普惠与小牛在线形成资金、资产端的闭环是符合商业逻辑的，在2015-2016年，大批互联网金融平台因为缺乏优质资产，加之监管冲击相继倒闭的情况下，小牛普惠、小牛在线互为补充，保持了业务的稳健和可持续发展。
				</p>
				<p>
					  “现在，小牛金服发展出来的新资产端——前海融辉及线上自动化借贷项目牛贷，借款人分布和信用状况不尽相同，这就要求我们在资金来源上，根据不同的资金成本做好开放引入，分级分散；小牛在线近400万的用户，他们的投资需求也是多样化的，这同样要求我们，根据不同的资产风险进行开放引入，分级分散。”只有将两端的闭环打开，才能服务更多的人群，做到“普”，才能创造更大的利润，做到“惠”。
				</p>
				<img src="<?php echo $filePath;?>fing25_6.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p>
					   第二部分则是打开公司治理闭环，引入多层级战略投资伙伴，成为公众公司。
				</p>
				<p>
					 “小牛金服发展得非常快，体量也在行业处于领先地位，但它仍然是一家创业公司。”创新、创业一直是小牛的主旋律，在新的阶段，如何在规范发展中创新，如何将创业进行得更彻底，如何让团队实现责任共同体、利益共同体、事业共同体。“这是我们一直在思考的问题。”刘金科说，打开公司治理闭环，首先是内部管理机制的打开，用管理创新激励和推进事业的发展，“即将执行的合伙人制是美好伙伴计划的基础内容。”
				</p>
				<p>
					合伙人制度这种形式，在西方国家是一种运作得非常成熟的管理架构，诸如摩根、美林、高盛等优秀的投资银行均是通过合伙人制度发展起来并成为全球优秀投资银行的。即将在小牛金服体系内执行的合伙人制度将基于小牛金服的发展战略，以利润为导向，形成责任风险共担，责权利对等的一种制度，“通过合伙人制度将能力最优秀也是流动性最高的业内精英集结在一起，形成一种独特、稳定而有效的管理架构，让公司真正的保持长效稳定的发展。”刘金科说。
				</p>
				<p style="max-width: 100%; min-height: 1em; color: rgb(62, 62, 62); font-size: 16px; line-height: 1.75em; box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(255, 255, 255);"><span style="max-width: 100%; font-size: 12px; color: rgb(255, 104, 39); text-decoration: underline; box-sizing: border-box !important; word-wrap: break-word !important;">☞美好伙伴的引入将在包括小牛金服在内的所有下属公司展开，是多层级的，包括但不限于金融机构、著名风投、同业优秀伙伴和领先科技企业。</span></p>
				<img src="<?php echo $filePath;?>fing25_7.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p>
					此外，小牛金服正在引入多层级的战略投资伙伴，这是实现海外上市，成为一家公众公司必须迈出的一步。刘金科对这个多层级的理解是，资产与资金、线上与线下，它们的资源能力、价值判断有不同的评价维度，它们对于战略投资伙伴各有所需。刘金科认为，引入多层级的战略投资人，能为小牛金服及旗下四大平台带来不限于资金、资源等多方面的裨益。
				</p>
				<p style="max-width: 100%; min-height: 1em; color: rgb(62, 62, 62); font-size: 16px; line-height: 1.75em; box-sizing: border-box !important; word-wrap: break-word !important; background-color: rgb(255, 255, 255);"><span style="max-width: 100%; font-size: 12px; color: rgb(255, 104, 39); text-decoration: underline; box-sizing: border-box !important; word-wrap: break-word !important;">☞小牛在线代言人大鹏来到发布会，与来宾们互动，为小牛美好家庭金融战略助力。</span></p>
				<img src="<?php echo $filePath;?>fing25_8.jpg" style="display: block; margin:0 auto; max-width:500px;"/>
				<p>
					 首先，站在未来的上市主体小牛金服的层面而言，引入美好伙伴能够帮助金服改善股东结构，建立起有利于今后上市的监管体系、法律框架和财务制度。同时，引入外部资本可以帮助金服迅速扩大规模，提高上市的估值和吸引投资者的注意，其次，对于全体金服员工而言，有效解决内部员工激励问题，建立完善的员工激励机制。
				</p>
				<p>
					再次，回归到金服旗下的四大平台，外部合作伙伴（银行、同业优秀伙伴、高科技公司）的加入能够给金服这个体系内的公司带来先进的运作经验和战略资源，帮助各平台更加快速地成长和成熟。同时，与同业伙伴间的合作有可能产生立竿见影的协同效应，迅速改善公司的成本收入结构，提高核心竞争力并最终带来公司业绩的提升。
				</p>
				<p>
					  “从行业的演进来看，过去通过简单复制，缺少核心技术的商业模式已经难以为继。不管是市场还是资本都越来越看重企业的差异化竞争力和以技术驱动创新的能力。”刘金科说，“未来小牛金服将围绕小微企业和家庭金融超级入口战略，持续推进业务演进和价值升级，而现在，我们显然有了一个非常好的基础。”
				</p>
