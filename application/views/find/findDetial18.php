				<p>记得不久前，为大家普及了“科技金融”（FinTech）一词，随着近几年科技、经济等一系列外部环境及条件的变化，金融已经不只是以前简单的金融，它被添加了众多元素。这不，继“科技金融”一词之后，“绿色金融”这一新词也悄然走进了人们的视野。G20峰会即将在中国杭州举行。作为全球经济治理重要的合作平台，今年G20的主题为“构建创新、活力、联动、包容的世界经济”。在此背景下，如何提振全球经济、推动国际金融监管体系改革等都将是本次G20峰会的重要议题。作为主办方的中国也在本次峰会首次将“绿色金融”话题引入G20议程。</p>
				<section class="" style="   box-sizing: border-box; " powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="display: inline-block; width: 100%; border-left-color: rgb(74, 112, 52); border-right-color: rgb(74, 112, 52); border-style: solid; border-width: 0px 0px 0px 6px; padding: 10px; box-sizing: border-box;"><section class="" style="box-sizing: border-box;" powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="color: rgb(74, 112, 52); box-sizing: border-box;"><section style="box-sizing: border-box;"><span style="font-size: 16px;"><strong style="box-sizing: border-box;">环境问题不容小视 金融被涂上“绿色”</strong></span></section></section></section></section></section></section></section>
				<p>什么是绿色金融？其实不难理解，关键词就在“绿色”和“金融”，说到金融，大家已经耳熟能详了，这儿就不解释了。而说到绿色，其实大家更多的想到的环保，近几年来，大家都提供绿色出行、绿色能源，所以也不难理解，绿色金融在很大程度上与环保有关。</p>
				<p>绿色金融，是指金融部门将环境保护作为一项基本政策，将环境影响的潜在的回报、风险和成本作为重要的因素，纳入投资决策和日常业务中，以此引导社会经济资源向环境保护、资源节约相关领域集聚，从而促进经济可持续发展的一种创新性的金融模式。与传统金融相同，绿色金融以市场收益为导向，实现对社会资源的引导和再配臵；而与传统金融不同，绿色金融关注环境和社会效益，将对环境的保护和对资源的有效利用作为计量金融机构活动成效的标准之一，追求金融活动与环境保护、生态平衡的协调。</p>
				<p>绿色金融与传统金融中的政策性金融有共同点，即它的实施需要由政府政策做推动，但传统金融以经济效益为目标，而绿色金融则更倾向于政策推动型，因其本身具有社会经济属性。自上个世纪80年代起，全世界对于气候与环境的关注开始从民间运动上升到政府间谈判合作的重要议题之一，绿色低碳的概念也逐渐开始融入到包括金融在内的经济部门发展中。2015年年末，196个缔约方聚集在法国巴黎达成了自1997年《京都议定书》以来具有里程碑意义的气候变化全球治理协定《巴黎协议》。这当中不仅设立了控制全球平均气温较工业化前水平升高2摄氏度之内的理想目标，也关注到了实现目标所需的资金、技术支持等问题。</p>
				<img src="/data/img/findImg/fing17_2.png" style="    display: block; margin:0 auto"/>
				<section class="" style="   box-sizing: border-box; " powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="display: inline-block; width: 100%; border-left-color: rgb(74, 112, 52); border-right-color: rgb(74, 112, 52); border-style: solid; border-width: 0px 0px 0px 6px; padding: 10px; box-sizing: border-box;"><section class="" style="box-sizing: border-box;" powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="color: rgb(74, 112, 52); box-sizing: border-box;"><section style="box-sizing: border-box;"><span style="font-size: 16px;"><strong style="box-sizing: border-box;">绿色金融前景良好 中国甘当引领者</strong></span></section></section></section></section></section></section></section>
				<p>中国选择在此次G20峰会上将绿色金融作为重点议题，其中一个重要原因是从总体上看到当前全球绿色金融发展有良好的发展前景：一方面，各国政府开始重视气候、环境问题，并切实地采取行动落实自主贡献预案，为全球绿色金融发展提供了良好的国际环境；另一方面，加强环保不仅仅只是成本，其实也同时为经济打开了盈利之门，包括新能源在内的新兴产业，拥有很广阔的市场前景，为绿色金融的发展奠定商业基础</p>
				<p>在环保社会公共意识的推动下，中国正在采取切实的行动，落实《巴黎协议》的自主贡献预案，也同时自觉地成为了世界绿色金融的引领者，这为在G20平台上推广绿色金融打下基础。中国不仅在国家宏观层面将绿色发展写入了“十三五”规划，而且正在努力建设绿色金融体系，并取得了很大成果。首先，中国正在努力发展绿色债券市场，今年上半年中国已经成为全世界最大的绿债市场，绿色债券发行量全球第一，达549亿元，占全球29.3%，全年发行量有望达到3000亿元人民币，与去年全球总量相当。其次，中国计划在明年建成全国统一碳市场，很可能将超越欧盟碳市场成为全球最大碳市场。</p>
				<img src="/data/img/findImg/fing17_3.png" style="    display: block; margin:0 auto"/>
				<section class="" style="   box-sizing: border-box; " powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="display: inline-block; width: 100%; border-left-color: rgb(74, 112, 52); border-right-color: rgb(74, 112, 52); border-style: solid; border-width: 0px 0px 0px 6px; padding: 10px; box-sizing: border-box;"><section class="" style="box-sizing: border-box;" powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="color: rgb(74, 112, 52); box-sizing: border-box;"><section style="box-sizing: border-box;"><span style="font-size: 16px;"><strong style="box-sizing: border-box;">我国绿色金融的发展明显加快</strong></span></section></section></section></section></section></section></section>
				<p>目前可以说，中国已开始引领全球绿色发展，这种“引领”表现在三个方面：第一，顶层设计。在国内，中国在“十三五规划”首次将“绿色”作为五大发展理念之一，这意味着，中国国内绿色发展的一系列顶层政策制度设计和实际操作都将在十三五期间获得迅速推进。9月举行的杭州G20会议上，预计将因东道主中国的推动而会首次将绿色发展纳入议题，由此将标志全球气候政治进入到了更强协调能力和执行力的新阶段；第二，中国绿债发行全球领先。2016年上半年国内绿债发行量独占鳌头，占到全球1/3；离岸绿债发行量名列全球第三；第三，2017年一旦统一碳市场建成，预计规模为全球最大。经过国内七大区域碳市场的试点，预计将在2017年启动全国统一碳市场，届时将赶超EU-ETS成为全球最大碳市场，目前相关准备工作正在加紧推进。</p>
				<p>当前我国绿色金融的发展步伐明显加快，在多个领域实现了创新和突破。一是绿色金融的政策框架逐步明晰。2015年12月人民银行和绿色金融专业委员会发布了《绿色金融债公告》和《绿色债券支持项目目录》，我国绿色债券市场正式启动。今年3月上海证券交易所发布的《关于开展绿色公司债试点的通知》积极引导公司债市场支持绿色产业发展。目前人民银行正在推动绿色金融的地方试点工作。</p>
				<img src="/data/img/findImg/fing17_4.png" style="    display: block; margin:0 auto"/>
				<section class="" style="   box-sizing: border-box; " powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="display: inline-block; width: 100%; border-left-color: rgb(74, 112, 52); border-right-color: rgb(74, 112, 52); border-style: solid; border-width: 0px 0px 0px 6px; padding: 10px; box-sizing: border-box;"><section class="" style="box-sizing: border-box;" powered-by="xiumi.us"><section class="" style="   box-sizing: border-box; "><section class="" style="color: rgb(74, 112, 52); box-sizing: border-box;"><section style="box-sizing: border-box;"><span style="font-size: 16px;"><strong style="box-sizing: border-box;">融资缺口仍大 绿色金融任重道远</strong></span></section></section></section></section></section></section></section>
				<p>伴随着绿色发展上升到国家战略层面，节能环保市场需求剧增。无论是环境污染治理、产业转型升级都离不开金融的支撑作用。实现绿色发展亟须改变资金配置方式，引导社会资本从能耗较大污染较重的行业、企业中退出，转向绿色、环保的领域。研究显示，从2015年到2020年，中国绿色发展的相应投资需求约为每年2.9万亿元人民币，其中政府的出资比例只占10%到15%。这表明超过80%的资金需要政府之外的社会资本来解决，绿色发展融资需求缺口巨大。</p>
				<p>小牛新财富的专家认为，绿色发展融资缺口的填补须依靠多样化的金融手段与融资机制，比如建立绿色金融体系，通过一系列的政策、制度安排，通过贷款、私募、投资、股票发行、债券、保险等方式改进金融和金融服务。近年来，针对绿色金融，我国实施了一系列政策和研究实践，绿色信贷、证券、债券等金融手段也已成环境保护的重要手段。相关领导表示，“今年年内，银监会还会对银行业等金融机构实施绿色银行评级，推动银行业金融机构加快向绿色金融机构转变。</p>
				<img src="/data/img/findImg/fing17_5.png" style="    display: block; margin:0 auto"/>
				<p>在机遇与挑战并存的情况下，中国此次将绿色金融作为G20的重点议题进行探讨，实际上是为了更好地在世界范围内寻求合作，推动世界经济和本国经济绿色转型。虽然当前世界经济不景气，各国财力有限，但是全球绿色低碳产业正处于起步阶段，具有广阔的市场前景，在全球经济转型的过程中，“绿色金融”很可能成为未来经济增长点，是现代金融业发展的一个重要趋势，中国在G20推动发展正当时，小牛新财富将与“绿色金融”创新同步。</p>
