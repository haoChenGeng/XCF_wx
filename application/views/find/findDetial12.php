				<p>2014年5月，《国务院关于进一步促进资本市场健康发展的若干意见》出台，鼓励发展私募投资基金，被提上国家层面。2014年8月21日，《私募投资基金监督管理暂行办法》经证监会公布施行。英大证券研究所所长李大霄当时认为，“《办法》的正式出台意味着新一轮私募基金改革开启，私募基金将迎来大发展时代”。</p>
				<p>正是在此基础上，各种类型的私募公司，雨后春笋般出现。过去一年，光新成立的证券投资基金管理公司就有4773家，平均每天13家。按基金总规模划分，管理正在运行的基金规模在20亿元以下的私募基金管理人有21388家，占比达98%；20亿-50亿元的260家，50亿-100亿元的88家，100亿元以上的85家；增长率分别为219.08%、54.76%、46.67%、97.67%。</p>
