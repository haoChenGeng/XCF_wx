				<p>自从2015年下半年的股灾开始，不仅股民的日子不好过，私募基金的日子也开始大不如前，业绩下滑、净值逼近甚至破了清盘线及排名落后都是近一年来大多私募的心头病。而一些私募却不守规则及法律，扰乱了整个行业，导致投资者的利益受损。在这样的背景之下，近一年来，一直处于野蛮生长状态的私募队伍也开始受到监管和规范。</p>
				<p>近日，在深圳市投资基金同业公会、招商证券资产管理有限公司、信诚基金管理有限公司和广东港联律师事务所联合主办的“新规下资管业务发展研讨会”上，深圳市投资基金同业公会邱伟胜透露，今年下半年，中基协将搭建一套“7+2”的私募基金规则体系，即七个办法和两个指引，这其中包括今年7月份已开始实施的《私募投资基金募集行为管理办法》。可见，对于私募行业的监管规则越来越健全，监管条款也越来越细化了。</p>
