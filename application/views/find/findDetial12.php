				<p>在这样的乱像丛生的背景之下，行业的规范和监管刻不容缓。“7+2”规则引发了私募及投资者的关注，所谓“7+2”专项自律规则体系，由有关私募基金管理的7项办法和2项指引共同构筑而成；其中7项办法除上述制定中的4项外，还包括已公布的私募登记《备案办法》、《募集行为管理办法》和《信息披露管理办法》；而2项指引则是指私募基金的《合同内容与格式指引》和《内控指引》。</p>
				<p> “私募监管原来是真空地带，现在办法不断出台，把私募的整个运作监管得越来越细化。”一位接近监管层的私募机构负责人表示，“越来越多的业务有相应的文件来指引，其实这正是今年以来私募监管从严和‘扶优限劣’的表现。”</p>
