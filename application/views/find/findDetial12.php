				<p>正如世间不变的规律，某样东西只要经过疯狂生长，就必然会参差不齐。作为A股市场的生力军，阳光私募近年来取得飞速发展。但是，随着行业规模的不断扩大，私募存在的问题和风险也正不断凸显。在新形势下，阳光私募行业存在着诸多问题，同时，“空壳”、“跑路”公司时有出现，再加上行业监管趋严，专业度欠佳的机构和从业人员面临淘汰危机，私募行业人员流失率将进一步加大。</p>
