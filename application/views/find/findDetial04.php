                <p>
                    相较于传统的礼物，越来越多的家长想送一份更长远、更有深意的“理财礼物”给孩子，因此，将也保险纳入到给孩子的“六一”礼物清单之中，寄托了父母希望孩子健康成长的心愿。
                </p>
                <p>
                    目前市场上在售的少儿类保险产品种类较多，但大都集中在侧重少儿重疾及意外保障，以及兼具理财和保障功能的孩子教育金等险种上。“目前市场上大部分的教育险都是分红型或万能型的，所以在确保健康保障基础上，再购买分红险等产品做教育规划。”一位保险专家表示，保险的搭配原则是保障优先，保障产品是基础，所以给孩子买保险也要首先考虑健康险。而且，同样的额度，同样的保障年限，年龄越小所缴保费越少。
                </p>
                <p>
                    此外，通过投保，家长向孩子传授保险保障的观念，让孩子明白保险与保障在理财中的重要性。
