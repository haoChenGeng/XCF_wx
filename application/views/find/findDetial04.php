                <p>
                    网上海淘近年来已然成为儿童节消费的一大亮点。随着“六一”的到来，各电商竞相推出了优惠活动。奶粉、婴幼儿食品、童装、玩具等，几乎涵盖了全线的母婴类商品。随着电子产品的普及，深受学生偏爱的电子产品也销售火爆。“儿童节就要到了，学习机、iPad、智能手表等电子产品的销量大幅上升。”电商行业人士分析，6月电商大战更为激烈，5月没有参战的电商都将陆续加入大促的行列。
                </p>
                <p>
                    此外，学习用品、书籍向来是家长采购礼物的首选。“每年儿童节前后，来书店给孩子买礼物的家长就会增多。”朝阳路上一家新华书店的工作人员说。为迎接儿童节，一些书店将比较受学生和家长欢迎的书摆在显著位置，有些书店则推出购书送毛绒公仔的活动来吸引更多的儿童读者。
                </p>
