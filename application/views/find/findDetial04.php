                <p>
                    除了送礼物，一些家长更倾向于亲自陪伴孩子过一个难忘的儿童节，选择在“六一”儿童节前后带着孩子出游，也成为不少家长的消费首选。大多数家庭选择了团期1至2天的周边游产品，父母齐出动，主题乐园、海洋世界、动植物园成为最受欢迎的出游产品。
                </p>
                <p>
                    数据显示，近期各地主题乐园预订火爆，气温上升也使得亲子活动强势来袭。亲子产品4月以来近180%的环比增幅，预示着儿童节期间或迎上半年亲子游出游高峰。恰逢今年儿童节在周三，预计5月28日的周末将迎来亲子游与周末休闲双重需求的爆发，出游高峰将提前到来，同比增幅预计超过300%。
                </p>
