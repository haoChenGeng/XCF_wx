    <section style="font-family: 微软雅黑; white-space: normal;" label="powered by 135editor.com">
      <section style="border-width: 0px; border-color: currentColor; box-sizing: border-box;" data-id="84261">
        <section style="margin: -0.8em 0.3em; padding: 0.8em; box-sizing: border-box;" data-style="color: rgb(51, 51, 51); font-size: 1em; line-height: 1.75em; word-break: break-all; word-wrap: break-word; text-align: justify;">
          <p style="line-height: 1.75em;"><span style="font-size: 14px;">于是，我在书房感受着那些细小的温暖，勾勒出“做小才牛”的思路，与亲爱的各位同仁分享，并听取各个层级的声音，不厌细小，但求广阔，获得指正，引发共勉。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">首先，队伍会变小。小牛资本旗下6个板块，15000人以上队伍，可谓人力繁重，虽然诸君人人勤勉，但模式太重。线下有线下的风控面谈优势，也有巨大的烦恼。况且线上形态多姿，金融科技昌盛，流量场景丛生，就是为了压低人的因素、人的工资成本、人的管理成本。所以，2017年，队伍会有优化，会适当变小变精。这是规律使然，企业必须顺应。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">其次，模式变小。小牛以前想的模式太大，不是生态就是平台。2017年将伴随着一些生态平台企业的颓势，我们已经领悟到模式的限度在哪里，我们的业务聚焦点在哪里，比如，我们没有电商和商超的基础，3C分期的能力一般，那么我们就慢慢退让这个领域，我们立足于我们熟悉的细分领域，毕其功力于一点，撕开口子然后延伸，利润必须是最终最长远的故事，而不是短暂满足风投的估值故事。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">再次，管理半径要变小。小牛发展太过迅猛，队伍太快意味着科层结构的增长，以前我还可以跟中层骨干经常相聚，现在大部分时间被几十个高管围绕。而且从E、M到P序列，链条太长，中层骨干的声音已经很难传入耳中。猛然醒悟之后，我惊出一身冷汗，随后开了几场和中层的直接沟通会，既听到了他们直言不讳的批评，也看到了他们悄无声息的进步。就像王阳明说的，“人须在事上练”，小牛持续进步的源动力，并非完全是高管升级人才引进，而是小牛内部中坚力量的生长。由此，应该压缩层级，让结构更为扁平，让中层的“犇跑计划”优先于高层的“犇腾计划”，将公司价值分解到中层这些小干部身上，要将这些中层骨干变成小牛的股东，要启动中层持股计划，要相信他们，培养他们。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">再次，小牛FinTech的着力点要变小。我们的金融科技力量要围绕着我们的成本节约、战略调整、利润优先，不是追求探索性和前沿性，不是追求花架子的功夫。我们的金融科技是我们业务的第一推动力，而不是赶时髦的第一化妆力。我们要找到金融和科技的动态均衡点，找到风险和收益的动态平衡点，我们要找到和培养出能够将金融需求准确“翻译”成科技语言的干将。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">最后，将小牛的梦想数量做小。不是将梦想做小，相反，小牛的梦想依然是很大的，我们还是要实现和打造国内国外两个资本市场的上市平台，我们还是要沿着金融的主流脉络来做，我们要不断透明化，将自己变成公众公司，我们每天要逼问自己究竟创造了什么社会价值、客户价值、体验价值？金融是所有行业中最接近社会企业的属性，金融不能完全用利润为单一目标，金融有普惠价值、社会价值，金融不是仅仅为股东服务，也为更广泛的利益攸关者服务。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">我说的将小牛的梦想数量做小，是因为我很无奈，我发现小牛里面每个人都有梦想，15000人就有15000个梦想，有的高管总想板块独立自立为一个山头，有的中层总觉得自己的意见未被重视……小牛的梦想只能有一个，就是小牛究竟用什么样的姿态去犇跑下一个五年，我的梦想就是汇聚所有小牛人的激情和智识，来完成对小牛梦想的追逐。人生交契无老少，论交何必先同调。有些人可以成为追逐梦想过程中的战友，有些人只好中途搁下来聊天做朋友了。古话说得好，以财交者，财尽而交绝，以色交者，华落而爱渝。我不会抱怨任何人，但我希望我们真正的兄弟姐妹们能汇聚在一起，用一种步调形成同一种力量。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">亲爱的同事们，凛冬已至，但我们使劲抱抱，照样可以让冬天变暖。亲爱的同事们，做小才能变牛，不是我们不要规模和增长，而是要用一种更理性更严峻的目光审视未来，将难度想高想大一点，将工作做小做细一点。2017年并不容易，经济会有大震荡，行业会有大变局，我们要主动调整，不要迟疑，不要侥幸，要轻装上场，英雄起于时势，将军出于行伍。对一些人他们感到困瑟，但另外一些人会觉得这是嘚瑟的机会。</span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
            </span></p>
          <p style="line-height: 1.75em;"><span style="font-size: 14px;">我们会过好2017年，而且会暖暖地过好。</span></p>
