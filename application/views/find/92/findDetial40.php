        <section style="border-width: 0px; border-color: currentColor; margin: 5px auto; box-sizing: border-box;">
          <section style="margin: -0.8em 0.3em; padding: 0.8em; color: inherit; box-sizing: border-box;" data-style="color: rgb(51, 51, 51); font-size: 1em; line-height: 1.75em; word-break: break-all; word-wrap: break-word; text-align: justify;">
            <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
              </span></p>
            <p style="line-height: 1.75em;"><span style="color: inherit; font-size: 14px;">2017年马上来了，先祝各位同事新年快乐，健康喜乐！</span><br>
            </p>
            <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
              </span></p>
            <p style="line-height: 1.75em;"><span style="font-size: 14px;">每到岁末年初，都涌动着很多复杂的情绪。人，对新的一年总是有着无限的期待和担忧，但对刚刚过去的一年，也有着很多不舍和遗憾。</span></p>
            <p style="line-height: 1.75em;"><span style="font-size: 14px;"><br>
              </span></p>
            <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">我在家里，在灯光的光晕下打字，听着小孩子发出的啼哭、厨房里面保姆洗刷的水流，心倒是无比沉静。平时应酬多，在家少，临近元旦倒是无处可去。总以为出去谈的都是大事大动作，现在窝在小书房里，听着这些温暖细小的声音，反而觉得非常充盈。</span></p>
            <p style="line-height: 1.75em;"><span style="color: inherit; line-height: 1.6; font-size: 14px;"></span><br>
            </p>
            <p style="text-align: justify; line-height: 1.75em;"><span style="font-size: 14px;">2016年小牛经历了太多的变化——从年初的队伍扩张高歌猛进，到年底的理性调整；从行业的普遍乐观，到监管的气氛肃杀；从人才升级的空降兵文化，到内部人晋升的优先通道；有时候想想很有意思，就像节气一样，我们真的从春走到了冬，身体从松松的，变成了紧紧的。</span></p>
            <p style="line-height: 1.75em;"><span style="font-family: 微软雅黑; font-size: 14px;"><br>
              </span></p>
            <p style="text-align: justify; line-height: 1.75em;"><span style="font-family: 微软雅黑; font-size: 14px;">前些日子，我去了一趟五台山，在禅宗祖庭思索，小牛何意？取名小牛，原意是阴阳互补，小是低调，牛是高调，其中普惠互联网金融板块做的是小人物的理财，让小人物牛起来就是新金融浪潮的平等化关键。但在名山古刹，突然有了新的感悟，对于现在而言，小牛不是并列词，而是先后序，做好了小，自然就会牛！</span></p>
          </section>
        </section>
      </section>
